// Contoh penggunaan operator aritmatika di dart

void main(){
    // Operator aritmatika adalah operator yang digunakan untuk melakukan operasi matematika
    // Operator aritmatika terdiri dari:
    // + untuk penjumlahan
    // - untuk pengurangan
    // * untuk perkalian
    // / untuk pembagian
    // % untuk modulus
    // ++ untuk increment
    // -- untuk decrement

    // Contoh penggunaan operator aritmatika di dart
    int a = 10;
    int b = 5;
    print ('$a + $b = ${a + b}');
    print ('$a - $b = ${a - b}');
    print ('$a * $b = ${a * b}');
    print ('$a / $b = ${a / b}');
    print ('$a % $b = ${a % b}');
    print ('$a++ = ${a++}');
    print ('$a-- = ${a--}');
}