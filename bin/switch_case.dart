// Contoh penggunaan switch case di dart

void main(){
    var nilai = 'C';
    switch(nilai){
        case 'A':
            print('Nilai kamu A');
            break;
        case 'B':
            print('Nilai kamu B');
            break;
        case 'C':
            print('Nilai kamu C');
            break;
        case 'D':
            print('Nilai kamu D');
            break;
        default:
            print('Nilai kamu E');
    }
}