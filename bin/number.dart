// Contoh penggunaan number di dart

void main (){
    // Bilangan number terkait value bisa dirubah dan nilai nya tidak tetap seperti contoh dibawah ini:

    // Bilangan bulat
    int number_1 = 10;
    number_1 = 20;
    // Bilangan desimal
    double number_2 = 10.5;
    number_2 = 20.5;

    print (number_1);
    print (number_2);

    // Num
    // Num berfungsi untuk mengetahui tipe data yang kita gunakan itu bilangan bulat & desimal
    // Jika menggunakan num maka value bisa dirubah seperti dibawah ini
    num number_3 = 10;
    number_3 = 20;
    num number_4 = 20.5;
    number_4 = 30.5;
    print (number_3);
    print (number_4);

}