// Contoh penggunaan operator perbandingan di dart

void main() {
  int a = 10;
  int b = 20;

  // Operator ==
  print(a == b); // false

  // Operator !=
  print(a != b); // true

  // Operator >
  print(a > b); // false

  // Operator <
  print(a < b); // true

  // Operator >=
  print(a >= b); // false

  // Operator <=
  print(a <= b); // true
}